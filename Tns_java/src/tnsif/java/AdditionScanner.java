package tnsif.java;
import java.util.*;
public class AdditionScanner {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter the number:");
		int a=sc.nextInt();
		int b=sc.nextInt();
		int c=a+b;
		System.out.println("Addition of a="+a+" and  b="+b+" is" +c);

	}

}
